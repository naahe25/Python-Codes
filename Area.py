              #Area Of Triangle

base = float( input ("Enter Base Of Triangle:") )
height = float ( input("Enter Height Of Triangle:"))

area = 0.5 * base * height

print("Area :" ,area)

                #Area Of Circle
                
radius = float( input ("Enter Radius Of Circle:") )


area = 3.1416 * radius * radius

