 #Numerical Operator

''' 
+ = Addition

- = Substruction

* = Multiplication

/ = Division

% = Modulus ( Remainder )

// = ( FLOOR )

** = Exponentiation (X^Y)

'''

a = 18
b = 4

print("Addition:" , (a + b) )

print("Substraction:" , (a - b) )

print("Multiplication:" , (a * b) )

print("Division:" , (a / b) )

print("Modulus:" , (a % b) )

print("Floor:" , (a // b) )

print("Exponential:" , (4 ** 2) ) # X^Y